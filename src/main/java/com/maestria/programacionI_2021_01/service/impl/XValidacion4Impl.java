package com.maestria.programacionI_2021_01.service.impl;

import com.maestria.programacionI_2021_01.service.XValidacion;
import javax.swing.JOptionPane;

/**
 *
 * @author lorenzolince
 */
public class XValidacion4Impl implements XValidacion {

    private int mes;

    public XValidacion4Impl() {
        this.mes = Integer.parseInt(JOptionPane.showInputDialog("Escribe el mes a evaluar:"));
    }

    @Override
    public void validar() {
        switch (mes) {
            case 1:
                System.out.println("Enero");
                break;
            case 2:
                System.out.println("Febrero");
                break;
            case 3:
                System.out.println("Marzo");
                break;
            case 4:
                System.out.println("Abril");
                break;
            case 5:
                System.out.println("May0");
                break;
            case 6:
                System.out.println("Junio");
                break;
            case 7:
                System.out.println("Julio");
                break;
            case 8:
                System.out.println("Agosto");
                break;
            case 9:
                System.out.println("Septiembre");
                break;
            case 10:
                System.out.println("Octubre");
                break;
            case 11:
                System.out.println("Noviembre");
                break;
            case 12:
                System.out.println("Diciembre");
                break;
            default:
                System.out.println("Mes Incorrecto");
                break;
        }
    }

}
